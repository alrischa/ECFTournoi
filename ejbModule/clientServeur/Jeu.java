public enum Jeu {
	Points(0), CrossJeu(1), Race07(2), AssettoCorsa(3), GTR2(4), RFactor2(5), GameStockCarE(6), R3E(7), GTLegends(8); 
	
	private int ID;
	public int getID(){
		return ID;
	}
	
	//*******************************************************************************************************************************
	//*******************************************************************************************************************************
	/**
	 * 
	 * @param id
	 */
	Jeu(int id){
		this.ID=id;
	}
	
	//*******************************************************************************************************************************
	//*******************************************************************************************************************************
	/**
	 * 
	 * @param id
	 * @return
	 */
	static Jeu getJeuFromId(int id){
		Jeu moyen=null;
		for(Jeu m:Jeu.values()){
			if (m.getID()==id){
				moyen=m;
			}
		}
		return moyen;
	}
	
}